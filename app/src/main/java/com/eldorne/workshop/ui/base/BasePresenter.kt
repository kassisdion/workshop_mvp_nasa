open class BasePresenter <T:BaseView> {


    /*
    ************************************************************************************************
    ** Private field
    ************************************************************************************************
     */


        protected var view: T? = null
            private set





    /*
   ************************************************************************************************
   **
   ************************************************************************************************
    */
    public fun onAttach(view: T) {
        this.view = view
    }

    public fun onDetach() {
        this.view = null
    }
}